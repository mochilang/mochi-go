package library

import (
	"fmt"
	"sort"
	"strings"
)

// renderGoMod renders the contents of go.mod. The output ends in a
// newline; the require block is omitted when deps is empty.
func renderGoMod(modulePath, goVersion string, deps map[string]string) string {
	var sb strings.Builder
	sb.WriteString("module ")
	sb.WriteString(modulePath)
	sb.WriteString("\n\n")
	sb.WriteString("go ")
	sb.WriteString(goVersion)
	sb.WriteString("\n")
	if len(deps) == 0 {
		return sb.String()
	}
	keys := make([]string, 0, len(deps))
	for k := range deps {
		keys = append(keys, k)
	}
	sort.Strings(keys)
	sb.WriteString("\nrequire (\n")
	for _, k := range keys {
		sb.WriteString("\t")
		sb.WriteString(k)
		sb.WriteString(" ")
		sb.WriteString(deps[k])
		sb.WriteString("\n")
	}
	sb.WriteString(")\n")
	return sb.String()
}

// renderDocGo renders doc.go: the package-level godoc comment plus
// the bare `package <name>` clause. When PublicAPI.Meta.DocComment is
// empty, Emit synthesises a stub comment that satisfies the
// `// Package <name> ...` first-sentence rule.
func renderDocGo(api PublicAPI) string {
	var sb strings.Builder
	doc := api.Meta.DocComment
	if doc == "" {
		doc = fmt.Sprintf("Package %s is generated by Mochi MEP-74.", api.PackageName)
	}
	writeGodoc(&sb, doc, "")
	if api.Meta.Repository != "" {
		sb.WriteString("//\n")
		writeGodoc(&sb, "Source: "+api.Meta.Repository, "")
	}
	if api.Version != "" {
		sb.WriteString("//\n")
		writeGodoc(&sb, "Version: "+api.Version, "")
	}
	sb.WriteString("package ")
	sb.WriteString(api.PackageName)
	sb.WriteString("\n")
	return sb.String()
}

// renderSource renders the main source file: `package <name>` plus
// the sorted public API. Extern items pull in `import "C"` and the
// rendered file is laid out so `goimports` is a no-op on it.
func renderSource(api PublicAPI) (string, error) {
	items := sortedItems(api.Items)

	var sb strings.Builder
	sb.WriteString("package ")
	sb.WriteString(api.PackageName)
	sb.WriteString("\n")

	if needsCgoImport(items) {
		sb.WriteString("\n")
		sb.WriteString("import \"C\"\n")
	}

	for _, item := range items {
		sb.WriteString("\n")
		if err := renderItem(&sb, item); err != nil {
			return "", err
		}
	}
	return sb.String(), nil
}

func needsCgoImport(items []Item) bool {
	for _, it := range items {
		if fn, ok := it.(ItemFunc); ok && fn.Extern {
			return true
		}
	}
	return false
}

func renderItem(sb *strings.Builder, item Item) error {
	switch it := item.(type) {
	case ItemConst:
		return renderConst(sb, it)
	case ItemVar:
		return renderVar(sb, it)
	case ItemType:
		return renderType(sb, it)
	case ItemFunc:
		return renderFunc(sb, it)
	default:
		return fmt.Errorf("%w: unknown Item type %T", ErrLibrary, item)
	}
}

func renderConst(sb *strings.Builder, it ItemConst) error {
	writeGodoc(sb, it.Doc, "")
	sb.WriteString("const ")
	sb.WriteString(it.Name)
	if it.Type != "" {
		sb.WriteString(" ")
		sb.WriteString(it.Type)
	}
	sb.WriteString(" = ")
	sb.WriteString(it.Value)
	sb.WriteString("\n")
	return nil
}

func renderVar(sb *strings.Builder, it ItemVar) error {
	writeGodoc(sb, it.Doc, "")
	sb.WriteString("var ")
	sb.WriteString(it.Name)
	if it.Type != "" {
		sb.WriteString(" ")
		sb.WriteString(it.Type)
	}
	if it.Value != "" {
		sb.WriteString(" = ")
		sb.WriteString(it.Value)
	}
	sb.WriteString("\n")
	return nil
}

func renderType(sb *strings.Builder, it ItemType) error {
	writeGodoc(sb, it.Doc, "")
	switch it.Kind {
	case "alias":
		sb.WriteString("type ")
		sb.WriteString(it.Name)
		sb.WriteString(" = ")
		sb.WriteString(it.Target)
		sb.WriteString("\n")
	case "definition":
		sb.WriteString("type ")
		sb.WriteString(it.Name)
		sb.WriteString(" ")
		sb.WriteString(it.Target)
		sb.WriteString("\n")
	case "struct":
		sb.WriteString("type ")
		sb.WriteString(it.Name)
		sb.WriteString(" struct {")
		if len(it.Fields) == 0 {
			sb.WriteString("}\n")
			return nil
		}
		sb.WriteString("\n")
		for _, f := range it.Fields {
			if f.Doc != "" {
				writeGodoc(sb, f.Doc, "\t")
			}
			sb.WriteString("\t")
			sb.WriteString(f.Name)
			sb.WriteString(" ")
			sb.WriteString(f.Type)
			if f.Tag != "" {
				sb.WriteString(" `")
				sb.WriteString(f.Tag)
				sb.WriteString("`")
			}
			sb.WriteString("\n")
		}
		sb.WriteString("}\n")
	case "interface":
		sb.WriteString("type ")
		sb.WriteString(it.Name)
		sb.WriteString(" interface {")
		if len(it.Methods) == 0 {
			sb.WriteString("}\n")
			return nil
		}
		sb.WriteString("\n")
		for _, m := range it.Methods {
			if m.Doc != "" {
				writeGodoc(sb, m.Doc, "\t")
			}
			sb.WriteString("\t")
			sb.WriteString(m.Name)
			sb.WriteString("(")
			renderParams(sb, m.Params)
			sb.WriteString(")")
			renderResults(sb, m.Results)
			sb.WriteString("\n")
		}
		sb.WriteString("}\n")
	default:
		return fmt.Errorf("%w: unknown ItemType.Kind %q", ErrLibrary, it.Kind)
	}
	return nil
}

func renderFunc(sb *strings.Builder, it ItemFunc) error {
	writeGodoc(sb, it.Doc, "")
	if it.Extern {
		sb.WriteString("//export ")
		sb.WriteString(it.Name)
		sb.WriteString("\n")
	}
	sb.WriteString("func ")
	sb.WriteString(it.Name)
	sb.WriteString("(")
	renderParams(sb, it.Params)
	sb.WriteString(")")
	renderResults(sb, it.Results)
	sb.WriteString(" {")
	body := strings.TrimRight(it.Body, "\n")
	if body == "" {
		sb.WriteString("}\n")
		return nil
	}
	sb.WriteString("\n")
	for _, line := range strings.Split(body, "\n") {
		if line == "" {
			sb.WriteString("\n")
			continue
		}
		sb.WriteString("\t")
		sb.WriteString(line)
		sb.WriteString("\n")
	}
	sb.WriteString("}\n")
	return nil
}

func renderParams(sb *strings.Builder, params []Param) {
	for i, p := range params {
		if i > 0 {
			sb.WriteString(", ")
		}
		if p.Name != "" {
			sb.WriteString(p.Name)
			sb.WriteString(" ")
		}
		sb.WriteString(p.Type)
	}
}

func renderResults(sb *strings.Builder, results []Result) {
	if len(results) == 0 {
		return
	}
	if len(results) == 1 && results[0].Name == "" {
		sb.WriteString(" ")
		sb.WriteString(results[0].Type)
		return
	}
	sb.WriteString(" (")
	for i, r := range results {
		if i > 0 {
			sb.WriteString(", ")
		}
		if r.Name != "" {
			sb.WriteString(r.Name)
			sb.WriteString(" ")
		}
		sb.WriteString(r.Type)
	}
	sb.WriteString(")")
}

// writeGodoc emits a doc comment with one `// ` prefix per line. The
// indent argument is prepended before the `//` so methods/fields can
// nest. Empty doc is a no-op.
func writeGodoc(sb *strings.Builder, doc, indent string) {
	if doc == "" {
		return
	}
	for _, line := range strings.Split(doc, "\n") {
		sb.WriteString(indent)
		if line == "" {
			sb.WriteString("//\n")
			continue
		}
		sb.WriteString("// ")
		sb.WriteString(line)
		sb.WriteString("\n")
	}
}

// renderReadmeScaffold synthesises a minimal README when the caller
// did not supply one. Scaffold mirrors the Mochi handbook convention.
func renderReadmeScaffold(api PublicAPI) string {
	var sb strings.Builder
	sb.WriteString("# ")
	sb.WriteString(api.PackageName)
	sb.WriteString("\n\n")
	if api.Meta.Description != "" {
		sb.WriteString(api.Meta.Description)
		sb.WriteString("\n\n")
	}
	sb.WriteString("Generated by Mochi MEP-74 from the Mochi package at version ")
	sb.WriteString(api.Version)
	sb.WriteString(".\n")
	sb.WriteString("\n## Installation\n\n")
	sb.WriteString("```sh\n")
	sb.WriteString("go get ")
	sb.WriteString(api.ModulePath)
	if api.Version != "" {
		sb.WriteString("@")
		sb.WriteString(api.Version)
	}
	sb.WriteString("\n```\n")
	if api.Meta.License != "" {
		sb.WriteString("\n## License\n\n")
		sb.WriteString(api.Meta.License)
		sb.WriteString("\n")
	}
	if len(api.Meta.Authors) > 0 {
		sb.WriteString("\n## Authors\n\n")
		authors := make([]string, len(api.Meta.Authors))
		copy(authors, api.Meta.Authors)
		sort.Strings(authors)
		for _, a := range authors {
			sb.WriteString("- ")
			sb.WriteString(a)
			sb.WriteString("\n")
		}
	}
	return sb.String()
}

// renderCHeader emits the cbindgen-analogue _cgo_export.h header.
// Only ItemFunc.Extern items with primitive-only signatures are
// declared; everything else lands as a `/* skipped */` comment.
func renderCHeader(api PublicAPI) string {
	var sb strings.Builder
	sb.WriteString("/* Generated by Mochi MEP-74 phase 11. Do not edit. */\n")
	guard := "MOCHI_" + strings.ToUpper(api.PackageName) + "_H"
	sb.WriteString("#ifndef ")
	sb.WriteString(guard)
	sb.WriteString("\n#define ")
	sb.WriteString(guard)
	sb.WriteString("\n\n")
	sb.WriteString("#include <stdint.h>\n")
	sb.WriteString("#include <stdbool.h>\n\n")
	sb.WriteString("#ifdef __cplusplus\nextern \"C\" {\n#endif\n\n")

	items := sortedItems(api.Items)
	for _, it := range items {
		fn, ok := it.(ItemFunc)
		if !ok || !fn.Extern {
			continue
		}
		writeCHeaderDecl(&sb, fn)
	}

	sb.WriteString("\n#ifdef __cplusplus\n}\n#endif\n\n")
	sb.WriteString("#endif /* ")
	sb.WriteString(guard)
	sb.WriteString(" */\n")
	return sb.String()
}

// writeCHeaderDecl renders one C declaration for an Extern ItemFunc
// into sb. Primitive Go-cgo types map to plain C types; non-primitive
// types are skipped with a comment.
func writeCHeaderDecl(sb *strings.Builder, fn ItemFunc) {
	if fn.Doc != "" {
		for _, line := range strings.Split(fn.Doc, "\n") {
			sb.WriteString("/* ")
			sb.WriteString(line)
			sb.WriteString(" */\n")
		}
	}
	retType, retOk := cTypeOf(returnType(fn.Results))
	if !retOk {
		sb.WriteString("/* skipped ")
		sb.WriteString(fn.Name)
		sb.WriteString(": non-primitive return */\n")
		return
	}
	cParams := make([]string, 0, len(fn.Params))
	skipped := ""
	for _, p := range fn.Params {
		ct, ok := cTypeOf(p.Type)
		if !ok {
			skipped = p.Type
			break
		}
		if p.Name == "" {
			cParams = append(cParams, ct)
		} else {
			cParams = append(cParams, ct+" "+p.Name)
		}
	}
	if skipped != "" {
		sb.WriteString("/* skipped ")
		sb.WriteString(fn.Name)
		sb.WriteString(": non-primitive param ")
		sb.WriteString(skipped)
		sb.WriteString(" */\n")
		return
	}
	if len(cParams) == 0 {
		cParams = []string{"void"}
	}
	sb.WriteString("extern ")
	sb.WriteString(retType)
	sb.WriteString(" ")
	sb.WriteString(fn.Name)
	sb.WriteString("(")
	sb.WriteString(strings.Join(cParams, ", "))
	sb.WriteString(");\n")
}

func returnType(results []Result) string {
	if len(results) == 0 {
		return ""
	}
	if len(results) > 1 {
		return "multi"
	}
	return results[0].Type
}

// cTypeOf maps a literal Go primitive to its plain-C type. Returns
// (ct, true) on success and ("", false) for non-primitive Go types
// or anything the bridge does not yet bind in plain C.
func cTypeOf(goType string) (string, bool) {
	switch goType {
	case "":
		return "void", true
	case "bool":
		return "bool", true
	case "int8":
		return "int8_t", true
	case "int16":
		return "int16_t", true
	case "int32", "rune":
		return "int32_t", true
	case "int64", "int":
		return "int64_t", true
	case "uint8", "byte":
		return "uint8_t", true
	case "uint16":
		return "uint16_t", true
	case "uint32":
		return "uint32_t", true
	case "uint64", "uint", "uintptr":
		return "uint64_t", true
	case "float32":
		return "float", true
	case "float64":
		return "double", true
	}
	return "", false
}
