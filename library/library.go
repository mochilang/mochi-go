// Package library is the MEP-74 phase 11 publish-direction emit
// layer. It owns the lowering of a Mochi package's public surface
// (the things its `pub fun` / `pub type` / `pub const` / `pub var`
// declarations expose) into a publishable Go module suitable for
// `mochi pkg publish --to=go+git+<repo-url>@<tag>`.
//
// The package is layering-conservative: it imports no other
// `package3/go/*` module and depends only on the Go stdlib. Callers
// in the MEP-54 build driver compose a PublicAPI from their own IR
// state and hand it to Emit; the rendered files are an in-memory
// map suitable for either disk materialisation or direct ingestion
// by a downstream `go build`.
//
// Per MEP-74 spec §3 Direction 2, Emit renders:
//
//   - go.mod with `module <vanity-path>` and the configured go
//     directive (defaulting to "1.21" so the embedded toolchain
//     accepts generics and the cgo handle pool API);
//   - doc.go carrying the package-level godoc comment that satisfies
//     the `// Package <name> ...` first-sentence rule;
//   - one `<name>.go` source file with the public API sorted by kind
//     (const, var, type, func) and then by name for byte-stable
//     diffs;
//   - optional README.md and LICENSE pass-throughs sourced from
//     mochi.toml;
//   - optional _cgo_export.h header for non-Go non-cgo consumers
//     when CHeader is set.
//
// The output is byte-deterministic: input slice permutations produce
// identical bytes and the EmitResult.Files keys are returned via the
// Names() helper in sorted order. Phase 12's git-tag publish flow
// computes a SHA-256 over the rendered files to attach to the
// `[[go-package]]` lockfile entry; that hash MUST be permutation-
// stable, which this package guarantees by sorting at every layer.
package library

import (
	"errors"
	"fmt"
	"sort"
	"strings"
	"unicode"
)

// ErrLibrary is the package-wide error sentinel for unrecoverable
// emit failures. Per-item validation failures embed it via
// errors.Wrap-style fmt.Errorf("%w: ...", ErrLibrary, ...) so callers
// can errors.Is-match without parsing message text.
var ErrLibrary = errors.New("library")

// PublicAPI is the lowered public surface of a Mochi package. It is
// the closed input shape Emit consumes; callers build it from their
// own IR state and the library package never reads it back.
type PublicAPI struct {
	// ModulePath is the vanity import path written to go.mod's
	// `module` directive, e.g. "github.com/mochilang/demo" or
	// "example.com/mochi/demo". Required.
	ModulePath string
	// PackageName is the Go package name used in every `package`
	// header. Must be a valid Go identifier (lowercase letter
	// start, ASCII letters/digits/underscores after). Required.
	PackageName string
	// Version is the published module version (semver tag form,
	// e.g. "v0.1.0"). Required. Recorded in doc.go and in the
	// README scaffolding but not in go.mod itself, since Go
	// modules carry their version through git tags.
	Version string
	// GoVersion is the value written to the `go` directive of
	// go.mod. Defaults to "1.21" when empty (the minimum version
	// the bridge supports). Use "1.22" or later for callers that
	// depend on `range over int` or the language changes in those
	// releases.
	GoVersion string
	// Items is the public API surface in caller order. Emit
	// re-sorts deterministically; callers can pass any order.
	Items []Item
	// Meta carries publish-side metadata sourced from mochi.toml.
	// All fields are optional; empty fields suppress their
	// associated render.
	Meta PackageMeta
	// Dependencies maps required module path to version
	// requirement, written to go.mod's `require` block.
	// Optional; an empty map omits the block entirely.
	Dependencies map[string]string
	// CHeader controls whether a plain-C header (_cgo_export.h)
	// is rendered alongside the Go sources. The header declares
	// every Extern-flagged ItemFunc with primitive-only
	// signatures; non-primitive signatures are skipped with a
	// comment. The header is the cbindgen analogue for
	// non-Go non-cgo consumers (e.g. C, C++, Mochi, Rust ffi).
	CHeader bool
}

// PackageMeta is the publish-side metadata block for the rendered
// Go module. Empty fields are omitted from the rendered files.
type PackageMeta struct {
	// DocComment is the package-level godoc string emitted into
	// doc.go. The first sentence MUST follow the godoc rule
	// "Package <name> <verb-phrase>." or `go doc` will not
	// surface the comment as the package summary. When empty,
	// Emit synthesises a stub "Package <name> is generated by
	// Mochi MEP-74." comment so doc.go always exists.
	DocComment string
	// Description is the one-line module description for the
	// README scaffold. Optional.
	Description string
	// License is the SPDX identifier (e.g. "MIT", "Apache-2.0",
	// "MIT OR Apache-2.0"). When empty, the README scaffold
	// omits the licence row. Distinct from LicenseText so
	// callers can pin the SPDX without shipping the full text.
	License string
	// LicenseText is the verbatim contents of the LICENSE file
	// at the module root. Optional; an empty value suppresses
	// the LICENSE artefact.
	LicenseText string
	// Repository is the canonical repo URL, recorded in the
	// README scaffold and in the doc.go header. Optional.
	Repository string
	// Authors is the author list for the README scaffold.
	// Optional.
	Authors []string
	// Readme is the verbatim README.md body. When non-empty,
	// overrides the scaffold; when empty and Description is
	// non-empty, Emit synthesises a minimal scaffold.
	Readme string
}

// Item is one renderable surface item. Implementations: ItemFunc,
// ItemType, ItemConst, ItemVar. The sealed interface keeps the
// surface closed and forces every consumer to type-switch
// exhaustively.
type Item interface {
	itemKind() itemKind
	itemName() string
}

type itemKind int

const (
	kindConst itemKind = iota
	kindVar
	kindType
	kindFunc
)

// ItemFunc is one exported function. The Body string is the
// already-lowered Go statement list excluding the surrounding
// braces; the caller owns the Mochi-to-Go expression lowering.
// When Extern is true, the rendered source carries a `//export
// <Name>` directive (cgo build mode), and the C header (when
// PublicAPI.CHeader is true) gets a matching declaration; non-
// primitive parameter or result types disqualify the item from
// the header with an explanatory comment.
type ItemFunc struct {
	Name    string
	Doc     string
	Params  []Param
	Results []Result
	Body    string
	Extern  bool
}

func (ItemFunc) itemKind() itemKind { return kindFunc }
func (f ItemFunc) itemName() string { return f.Name }

// Param is one function parameter. Type is a literal Go type
// string; the caller has already mapped Mochi to Go types via the
// typemap package.
type Param struct {
	Name string
	Type string
}

// Result is one function return value. Name is optional (Go
// supports named returns); Type is required.
type Result struct {
	Name string
	Type string
}

// ItemType is one exported type declaration. Kind selects the
// rendered shape: "struct" emits `type Name struct { ... }`,
// "alias" emits `type Name = Target`, "definition" emits
// `type Name Target` (a new named type sharing a representation),
// "interface" emits `type Name interface { ... }`.
type ItemType struct {
	Name    string
	Doc     string
	Kind    string
	Target  string
	Fields  []Field
	Methods []Method
}

func (ItemType) itemKind() itemKind { return kindType }
func (t ItemType) itemName() string { return t.Name }

// Field is one struct field. Tag is rendered verbatim (callers
// include surrounding backticks via TagLiteral when needed).
type Field struct {
	Name string
	Type string
	Tag  string
	Doc  string
}

// Method is one interface method signature.
type Method struct {
	Name    string
	Params  []Param
	Results []Result
	Doc     string
}

// ItemConst is one exported const declaration.
type ItemConst struct {
	Name  string
	Doc   string
	Type  string
	Value string
}

func (ItemConst) itemKind() itemKind { return kindConst }
func (c ItemConst) itemName() string { return c.Name }

// ItemVar is one exported var declaration. Value is optional; an
// empty value renders the zero-value declaration `var Name Type`.
type ItemVar struct {
	Name  string
	Doc   string
	Type  string
	Value string
}

func (ItemVar) itemKind() itemKind { return kindVar }
func (v ItemVar) itemName() string { return v.Name }

// EmitResult is the rendered file set. Files maps path-relative-to-
// module-root to file contents. The map is unordered; use Names to
// retrieve the sorted path slice for deterministic iteration.
type EmitResult struct {
	Files map[string]string
}

// Names returns the EmitResult.Files keys in sorted order.
func (r EmitResult) Names() []string {
	out := make([]string, 0, len(r.Files))
	for k := range r.Files {
		out = append(out, k)
	}
	sort.Strings(out)
	return out
}

// Emit renders the PublicAPI to an EmitResult. The output is
// byte-deterministic: permuting api.Items, api.Dependencies, or
// api.Meta.Authors produces identical bytes. Validation errors
// (missing required fields, illegal identifiers, unknown ItemType
// kind) surface as wrapped ErrLibrary.
func Emit(api PublicAPI) (EmitResult, error) {
	if err := validateAPI(api); err != nil {
		return EmitResult{}, err
	}
	goVersion := api.GoVersion
	if goVersion == "" {
		goVersion = "1.21"
	}

	files := map[string]string{}
	files["go.mod"] = renderGoMod(api.ModulePath, goVersion, api.Dependencies)
	files["doc.go"] = renderDocGo(api)

	src, err := renderSource(api)
	if err != nil {
		return EmitResult{}, err
	}
	files[api.PackageName+".go"] = src

	if api.Meta.Readme != "" {
		files["README.md"] = api.Meta.Readme
	} else if api.Meta.Description != "" {
		files["README.md"] = renderReadmeScaffold(api)
	}
	if api.Meta.LicenseText != "" {
		files["LICENSE"] = api.Meta.LicenseText
	}
	if api.CHeader {
		files["_cgo_export.h"] = renderCHeader(api)
	}
	return EmitResult{Files: files}, nil
}

// validateAPI checks the structural invariants Emit relies on.
func validateAPI(api PublicAPI) error {
	if api.ModulePath == "" {
		return fmt.Errorf("%w: ModulePath is required", ErrLibrary)
	}
	if !isModulePath(api.ModulePath) {
		return fmt.Errorf("%w: ModulePath %q is not a valid module path", ErrLibrary, api.ModulePath)
	}
	if api.PackageName == "" {
		return fmt.Errorf("%w: PackageName is required", ErrLibrary)
	}
	if !isGoIdentifier(api.PackageName) {
		return fmt.Errorf("%w: PackageName %q is not a valid Go identifier", ErrLibrary, api.PackageName)
	}
	if api.Version == "" {
		return fmt.Errorf("%w: Version is required", ErrLibrary)
	}
	for _, item := range api.Items {
		if err := validateItem(item); err != nil {
			return err
		}
	}
	return nil
}

func validateItem(item Item) error {
	switch it := item.(type) {
	case ItemFunc:
		if !isExportedIdent(it.Name) {
			return fmt.Errorf("%w: ItemFunc.Name %q must be an exported Go identifier", ErrLibrary, it.Name)
		}
	case ItemType:
		if !isExportedIdent(it.Name) {
			return fmt.Errorf("%w: ItemType.Name %q must be an exported Go identifier", ErrLibrary, it.Name)
		}
		switch it.Kind {
		case "struct", "alias", "definition", "interface":
		default:
			return fmt.Errorf("%w: ItemType.Kind %q is not one of struct/alias/definition/interface", ErrLibrary, it.Kind)
		}
		if (it.Kind == "alias" || it.Kind == "definition") && it.Target == "" {
			return fmt.Errorf("%w: ItemType.Target required for kind %q", ErrLibrary, it.Kind)
		}
	case ItemConst:
		if !isExportedIdent(it.Name) {
			return fmt.Errorf("%w: ItemConst.Name %q must be an exported Go identifier", ErrLibrary, it.Name)
		}
		if it.Value == "" {
			return fmt.Errorf("%w: ItemConst.Value required for %q", ErrLibrary, it.Name)
		}
	case ItemVar:
		if !isExportedIdent(it.Name) {
			return fmt.Errorf("%w: ItemVar.Name %q must be an exported Go identifier", ErrLibrary, it.Name)
		}
	default:
		return fmt.Errorf("%w: unknown Item type %T", ErrLibrary, item)
	}
	return nil
}

// sortedItems returns api.Items grouped by kind (const, var, type,
// func) and then by name within each kind. The grouping is the
// rendered file layout; callers can rely on the order being stable.
func sortedItems(items []Item) []Item {
	cp := make([]Item, len(items))
	copy(cp, items)
	sort.SliceStable(cp, func(i, j int) bool {
		ki, kj := cp[i].itemKind(), cp[j].itemKind()
		if ki != kj {
			return ki < kj
		}
		return cp[i].itemName() < cp[j].itemName()
	})
	return cp
}

// PascalCase returns name with each underscore-separated chunk
// title-cased and the underscores removed. Empty input returns
// empty output; non-ASCII rune chunks are passed through verbatim.
//
// "hello_world" -> "HelloWorld"
// "n_iter"      -> "NIter"
// "x"           -> "X"
//
// The function is exported because the MEP-54 build driver calls
// it directly to upgrade Mochi snake_case identifiers to Go's
// exported PascalCase convention before populating PublicAPI.Items.
func PascalCase(name string) string {
	if name == "" {
		return ""
	}
	parts := strings.Split(name, "_")
	var sb strings.Builder
	for _, p := range parts {
		if p == "" {
			continue
		}
		runes := []rune(p)
		runes[0] = unicode.ToUpper(runes[0])
		sb.WriteString(string(runes))
	}
	return sb.String()
}

func isGoIdentifier(s string) bool {
	if s == "" {
		return false
	}
	for i, r := range s {
		if i == 0 {
			if !(unicode.IsLetter(r) || r == '_') {
				return false
			}
			continue
		}
		if !(unicode.IsLetter(r) || unicode.IsDigit(r) || r == '_') {
			return false
		}
	}
	if isGoKeyword(s) {
		return false
	}
	return true
}

func isExportedIdent(s string) bool {
	if !isGoIdentifier(s) {
		return false
	}
	r := []rune(s)[0]
	return unicode.IsUpper(r)
}

func isModulePath(s string) bool {
	if s == "" {
		return false
	}
	if strings.HasPrefix(s, "/") || strings.HasSuffix(s, "/") {
		return false
	}
	if strings.Contains(s, "..") {
		return false
	}
	segs := strings.Split(s, "/")
	if len(segs) < 1 {
		return false
	}
	for _, seg := range segs {
		if seg == "" {
			return false
		}
		for _, r := range seg {
			if !(unicode.IsLetter(r) || unicode.IsDigit(r) ||
				r == '-' || r == '.' || r == '_' || r == '~') {
				return false
			}
		}
	}
	first := segs[0]
	if !strings.ContainsAny(first, ".") {
		return false
	}
	return true
}

var goKeywords = map[string]struct{}{
	"break": {}, "case": {}, "chan": {}, "const": {}, "continue": {},
	"default": {}, "defer": {}, "else": {}, "fallthrough": {},
	"for": {}, "func": {}, "go": {}, "goto": {}, "if": {},
	"import": {}, "interface": {}, "map": {}, "package": {},
	"range": {}, "return": {}, "select": {}, "struct": {},
	"switch": {}, "type": {}, "var": {},
}

func isGoKeyword(s string) bool { _, ok := goKeywords[s]; return ok }
