func TestWorkspaceModuleKindString(t *testing.T) {
	cases := map[WorkspaceModuleKind]string{
		ModuleUser:                  "user",
		ModuleWrapper:               "wrapper",
		ModuleRuntime:               "runtime",
		WorkspaceModuleKind(99):     "unknown",
	}
	for k, want := range cases {
		if got := k.String(); got != want {
			t.Errorf("(%d).String() = %q; want %q", int(k), got, want)
		}
	}
}

func TestCgoBuildModeString(t *testing.T) {
	cases := map[CgoBuildMode]string{
		CgoArchive:        "c-archive",
		CgoShared:         "c-shared",
		CgoNone:           "",
		CgoBuildMode(99):  "unknown",
	}
	for m, want := range cases {
		if got := m.String(); got != want {
			t.Errorf("(%d).String() = %q; want %q", int(m), got, want)
		}
	}
}

func equalSlices(a, b []string) bool {
	if len(a) != len(b) {
		return false
	}
	for i := range a {
		if a[i] != b[i] {
			return false
		}
	}
	return true
}
