func TestRenderGoWorkBasic(t *testing.T) {
	w := DefaultWorkspace()
	w.AddModule(WorkspaceModule{ImportPath: "mochi_user", Path: "mochi_user", Kind: ModuleUser})
	w.AddModule(WorkspaceModule{ImportPath: "wrap_cobra", Path: "go_wrap/github.com/spf13/cobra", Kind: ModuleWrapper})
	got := w.RenderGoWork()

	wantContains := []string{
		"Auto-generated by MEP-74 bridge",
		"go 1.21",
		"use (",
		"./mochi_user",
		"./go_wrap/github.com/spf13/cobra",
		")",
	}
	for _, sub := range wantContains {
		if !strings.Contains(got, sub) {
			t.Errorf("rendered go.work missing %q\n--- output ---\n%s", sub, got)
		}
	}
}

func TestRenderGoWorkSortsModules(t *testing.T) {
	w := DefaultWorkspace()
	w.AddModule(WorkspaceModule{ImportPath: "z", Path: "go_wrap/z", Kind: ModuleWrapper})
	w.AddModule(WorkspaceModule{ImportPath: "a", Path: "go_wrap/a", Kind: ModuleWrapper})
	got := w.RenderGoWork()
	idxA := strings.Index(got, "./go_wrap/a")
	idxZ := strings.Index(got, "./go_wrap/z")
	if idxA < 0 || idxZ < 0 || idxA > idxZ {
		t.Errorf("modules not sorted alphabetically:\n%s", got)
	}
}

func TestRenderGoWorkDeterministic(t *testing.T) {
	build := func() string {
		w := DefaultWorkspace()
		w.AddModule(WorkspaceModule{ImportPath: "z", Path: "go_wrap/z", Kind: ModuleWrapper})
		w.AddModule(WorkspaceModule{ImportPath: "a", Path: "go_wrap/a", Kind: ModuleWrapper})
		w.AddReplace("example.com/zlib", "../zlib")
		w.AddReplace("example.com/alpha", "../alpha")
		return w.RenderGoWork()
	}
	first := build()
	for i := range 10 {
		if got := build(); got != first {
			t.Fatalf("RenderGoWork is non-deterministic on iter %d:\n--- first ---\n%s\n--- got ---\n%s", i, first, got)
		}
	}
}

func TestRenderGoWorkReplaceSorted(t *testing.T) {
	w := DefaultWorkspace()
	w.AddReplace("example.com/zlib", "../zlib")
	w.AddReplace("example.com/alpha", "../alpha")
	w.AddReplace("example.com/mid", "../mid")
	got := w.RenderGoWork()

	idxAlpha := strings.Index(got, "replace example.com/alpha => ")
	idxMid := strings.Index(got, "replace example.com/mid => ")
	idxZ := strings.Index(got, "replace example.com/zlib => ")
	if !(idxAlpha < idxMid && idxMid < idxZ) {
		t.Errorf("replace directives not sorted alphabetically: alpha=%d mid=%d zlib=%d\n%s", idxAlpha, idxMid, idxZ, got)
	}
}

