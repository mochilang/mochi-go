func TestDefaultWorkspace(t *testing.T) {
	w := DefaultWorkspace()
	if w.GoVersion != "1.21" {
		t.Errorf("GoVersion = %q; want %q", w.GoVersion, "1.21")
	}
	if len(w.Modules) != 0 {
		t.Errorf("DefaultWorkspace().Modules = %d; want 0", len(w.Modules))
	}
	if w.CgoBuildMode != CgoArchive {
		t.Errorf("CgoBuildMode = %v; want CgoArchive", w.CgoBuildMode)
	}
	if !w.Trimpath {
		t.Errorf("Trimpath = false; want true")
	}
	if w.LdFlags != "-s -w" {
		t.Errorf("LdFlags = %q; want -s -w", w.LdFlags)
	}
	if w.ReplaceDirectives == nil {
		t.Errorf("ReplaceDirectives should be non-nil map for ergonomic AddReplace")
	}
}

func TestAddModuleKeepsSorted(t *testing.T) {
	w := DefaultWorkspace()
	w.AddModule(WorkspaceModule{ImportPath: "mochilang.local/go_wrap/cobra", Path: "go_wrap/github.com/spf13/cobra", Kind: ModuleWrapper})
	w.AddModule(WorkspaceModule{ImportPath: "mochilang.local/go_wrap/uuid", Path: "go_wrap/github.com/google/uuid", Kind: ModuleWrapper})
	w.AddModule(WorkspaceModule{ImportPath: "mochi_user", Path: "mochi_user", Kind: ModuleUser})
	got := []string{}
	for _, m := range w.Modules {
		got = append(got, m.Path)
	}
	want := []string{
		"go_wrap/github.com/google/uuid",
		"go_wrap/github.com/spf13/cobra",
		"mochi_user",
	}
	if !equalSlices(got, want) {
		t.Errorf("Modules order = %v; want %v", got, want)
	}
}

