func TestAddReplaceReplacesExisting(t *testing.T) {
	w := DefaultWorkspace()
	w.AddReplace("example.com/foo", "../local-foo")
	w.AddReplace("example.com/foo", "../newer-local-foo")
	if got := w.ReplaceDirectives["example.com/foo"]; got != "../newer-local-foo" {
		t.Errorf("ReplaceDirectives[foo] = %q; want ../newer-local-foo", got)
	}
}

