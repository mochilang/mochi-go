package build

import (
	"os"
	"path/filepath"
	"strings"
	"testing"
)

// TestPhase0Skeleton is the per-phase sentinel test. The CI gate for phase
// 0 LANDED requires this test to pass on every supported host.
//
// Sub-tests:
//   - end_to_end: Driver allocates work-dir, writes workspace root,
//     re-reads it, the go.work parses to expected content, cleanup
//     removes the work-dir.
//   - package_layout: the package3/go/ tree exists at the expected paths
//     and has the documented Go packages.
//   - default_workspace_invariants: DefaultWorkspace() validates and
//     renders the auto-generated header plus the `go` line.
func TestPhase0Skeleton(t *testing.T) {
	t.Run("end_to_end", func(t *testing.T) {
		d := NewDriver(Options{NoCache: true})
		defer d.Cleanup()

		w, err := d.PrepareWorkspace()
		if err != nil {
			t.Fatalf("PrepareWorkspace: %v", err)
		}
		w.AddModule(WorkspaceModule{ImportPath: "mochi_user", Path: "mochi_user", Kind: ModuleUser})
		w.AddModule(WorkspaceModule{ImportPath: "wrap_cobra", Path: "go_wrap/github.com/spf13/cobra", Kind: ModuleWrapper})
		w.AddReplace("example.com/foo", "../local_foo")

		root, err := d.WriteWorkspaceRoot(w)
		if err != nil {
			t.Fatalf("WriteWorkspaceRoot: %v", err)
		}
		manifestBytes, err := os.ReadFile(filepath.Join(root, "go.work"))
		if err != nil {
			t.Fatalf("read go.work: %v", err)
		}
		manifest := string(manifestBytes)
		wantBits := []string{
			"go 1.21",
			"use (",
			"./mochi_user",
			"./go_wrap/github.com/spf13/cobra",
			"replace example.com/foo => ../local_foo",
		}
		for _, bit := range wantBits {
			if !strings.Contains(manifest, bit) {
				t.Errorf("go.work missing %q\n--- output ---\n%s", bit, manifest)
			}
		}

		work := d.WorkDir()
		if err := d.Cleanup(); err != nil {
			t.Fatalf("Cleanup: %v", err)
		}
		if _, err := os.Stat(work); !os.IsNotExist(err) {
			t.Errorf("work-dir not removed by Cleanup: %v", err)
		}
	})

	t.Run("package_layout", func(t *testing.T) {
		here, err := os.Getwd()
		if err != nil {
			t.Fatalf("Getwd: %v", err)
		}
		pkgRoot := filepath.Dir(here)
		expected := []string{
			"README.md",
			"build/driver.go",
			"build/workspace.go",
			"errors/errors.go",
		}
		for _, rel := range expected {
			path := filepath.Join(pkgRoot, rel)
			info, err := os.Stat(path)
			if err != nil {
				t.Errorf("expected file %s missing: %v", rel, err)
				continue
			}
			if info.IsDir() {
				t.Errorf("expected file %s is a directory", rel)
			}
		}
	})

	t.Run("default_workspace_invariants", func(t *testing.T) {
		w := DefaultWorkspace()
		if err := w.Validate(); err != nil {
			t.Errorf("DefaultWorkspace().Validate() = %v; want nil", err)
		}
		manifest := w.RenderGoWork()
		if !strings.Contains(manifest, "Auto-generated by MEP-74") {
			t.Errorf("DefaultWorkspace render missing header")
		}
		if !strings.Contains(manifest, "go 1.21") {
			t.Errorf("DefaultWorkspace render missing go-version directive")
		}
		if w.CgoBuildMode.String() != "c-archive" {
			t.Errorf("default CgoBuildMode = %q; want c-archive", w.CgoBuildMode.String())
		}
	})
}
